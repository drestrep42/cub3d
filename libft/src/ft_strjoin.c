#include "../inc/libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	size_t	i;
	size_t	j;
	char	*s3;

	i = 0;
	j = 0;
	if (s1 == NULL || s2 == NULL)
		return (NULL);
	s3 = (char *)malloc ((ft_strlen
				((char *)s1) + ft_strlen((char *)s2) + 1) * sizeof(char));
	if (s3 == NULL)
		return (NULL);
	s3[ft_strlen((char *)s1) + ft_strlen((char *)s2)] = '\0';
	while (s1[i])
	{
		s3[i] = s1[i];
		i++;
	}
	while (s2[j])
	{
		s3[i + j] = s2[j];
		j++;
	}
	return (s3);
}
