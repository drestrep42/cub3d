#include "../inc/libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*aux;

	if (!new)
		return ;
	if (lst)
	{
		if (*lst != NULL)
		{
			aux = *lst;
			while (aux->next)
				aux = aux->next;
			aux->next = new;
			return ;
		}
		*lst = new;
		return ;
	}
}
